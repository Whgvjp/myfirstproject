package data1108;

/**
 * 比较运算符：
 * 大于：      >
 * 小于：      <
 * 小于等于：  <=
 * 大于等于：  >=
 * 相等：      ==
 * 不相等：    !=
 */
public class Demo08Operator {
    public static void main(String[] args) {
        System.out.println(10 > 5);
        int num1 = 10;
        int num2 = 12;
        System.out.println(num1 < num2);
        System.out.println(num2 >= 100);



    }
}
